export class Author {
    constructor(id, name, username, email, phone, street, city, zipCode, companyName, slogan) {
        this.id = id;
        this.name = name;
        this.username = username;
        this.email = email;
        this.phone = phone;
        this.street = street;
        this.city = city;
        this.zipCode = zipCode;
        this.companyName = companyName;
        this.slogan = slogan;
    }
}
